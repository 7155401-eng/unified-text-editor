var CARI_STYLES = {
  "איור מצחיק/הומוריסטי/משעשע": "funny, humorous, amusing, exaggerated caricature",
  "איורי ילדים": "playful children's book illustration",
  "איור אמנותי": "fine artistic illustration"
};


function _cariStyleFallbackKey() {
  return "איור מצחיק/הומוריסטי/משעשע";
}


function _cariStyleWord(key) {
  return CARI_STYLES[key] || CARI_STYLES[_cariStyleFallbackKey()];
}


// ──────────────────────────────────────────────────────────────────
// בניית פרומפט ליטוש — שולח את ההנחיות המלאות + הקלט הגולמי לטקסט גמיני
// ──────────────────────────────────────────────────────────────────
function _cariBuildPolishPrompt(userScene, styleKey) {
  return (
    CARI_FULL_INSTRUCTIONS + "\n\n" +
    "═══════════════════════════════════════════════════════════════\n" +
    "POLISH TASK: Take the user's brief scene below and enrich it into a " +
    "single tight English paragraph (3-5 short sentences) that an " +
    "image-generation model can render directly. Apply ALL the rules " +
    "above. Output ONLY the refined paragraph, no preamble.\n" +
    "Humor flavor (locked): " + _cariStyleWord(styleKey) + ".\n" +
    "User's brief scene (translate Hebrew faithfully):\n" +
    CARI_HIDDEN_PREFIX + (userScene || "a moment of joyful Torah life") + "\n\n" +
    "Refined scene paragraph:"
  );
}


// ──────────────────────────────────────────────────────────────────
// בניית פרומפט תמונה — מחזיר {systemText, userText} בנפרד.
// ──────────────────────────────────────────────────────────────────
function _cariBuildImageRequest(scene, styleKey, negative) {
  scene = (scene || "").toString().trim();
  if (!scene) scene = "a moment of joyful Torah life";
  // פרפיקס נסתר: מתווסף לפני הסצנה של המשתמש בצד השרת בלבד.
  var prefixedScene = CARI_HIDDEN_PREFIX + scene;
  var styleWord = _cariStyleWord(styleKey);
  var avoid = (negative && negative.toString().trim())
              ? "\n\nAvoid in the image: " + negative.toString().trim() + "."
              : "";
  // System: ההנחיות המלאות מילה-במילה.
  // User: רק הסצנה של המשתמש + הסגנון + מה לא לכלול.
  return {
    system: CARI_FULL_INSTRUCTIONS,
    user: ("Scene to draw: " + prefixedScene + "\n\n" +
           "Humor flavor: " + styleWord + "." +
           avoid + "\n\n" +
           "Generate one " + styleWord + " caricature image now, " +
           "applying every rule from the system instructions.\n\n" +
           "FINAL CRITICAL REMINDERS (these override anything else):\n" +
           "1. ABSOLUTELY NO signature, NO watermark, NO artist name, " +
           "NO initials, NO logo, NO copyright mark anywhere in the " +
           "image. Scan all four corners — the image must be 100% " +
           "clean of any identifying mark or text in the corners. " +
           "A signed image is a FAILED image; do not output it.\n" +
           "2. Men and boys ONLY — zero women, zero girls, zero " +
           "female figures of any kind, foreground or background.\n" +
           "3. Authentic Haredi/Yeshivish dress (black hat or " +
           "shtreimel, frock coat, beard, payot).\n" +
           "4. Warm smiling expressions on every figure.\n" +
           "5. Exactly 5 fingers per hand, 2 arms, 2 legs per figure.")
  };
}


// ──────────────────────────────────────────────────────────────────
// Gemini text — לשלב הליטוש האופציונלי.
// ──────────────────────────────────────────────────────────────────
function _cariCallGeminiText(prompt, apiKey) {
  var url = "https://generativelanguage.googleapis.com/v1beta/models/" +
            "gemini-2.0-flash:generateContent?key=" + encodeURIComponent(apiKey);
  var resp = UrlFetchApp.fetch(url, {
    method: "post",
    contentType: "application/json",
    payload: JSON.stringify({
      contents: [{ parts: [{ text: prompt }] }],
      generationConfig: { temperature: 0.8, maxOutputTokens: 600 }
    }),
    muteHttpExceptions: true
  });
  if (resp.getResponseCode() !== 200) return null;
  var data = JSON.parse(resp.getContentText());
  var cand = (data.candidates && data.candidates[0]) || {};
  var parts = (cand.content && cand.content.parts) || [];
  var out = parts.map(function (p) { return p.text || ""; }).join("").trim();
  return out || null;
}


// ──────────────────────────────────────────────────────────────────
// Imagen / fallback Gemini-Flash-Image.
// ──────────────────────────────────────────────────────────────────
// מודל יחיד ובלעדי ליצירת תמונות — ללא שרשרת fallback.
var _CARI_IMAGE_MODEL = "gemini-3-pro-image-preview";


function _cariCallGeminiImage(modelId, systemText, userText, refImages, aspect, count, apiKey) {
  // ההנחיות עוברות בשדה systemInstruction, הסצנה של המשתמש בשדה contents.
  // refImages: רשימה של {mime_type, data_base64} — תמונות הפניה לסגנון.
  // aspect: יחס מימדים — מועבר ב-generationConfig.imageConfig.aspectRatio
  // ומוטמע גם בטקסט הסצנה למקרה שהמודל לא מכבד את ה-imageConfig.
  var url = "https://generativelanguage.googleapis.com/v1beta/models/" +
            modelId + ":generateContent?key=" +
            encodeURIComponent(apiKey);
  // הסבת יחס לתיאור מילולי לחיזוק
  var aspectStr = (aspect || "1:1").toString().trim();
  var aspectMap = {
    "1:1": "square 1:1 aspect ratio image",
    "16:9": "wide landscape 16:9 aspect ratio image",
    "9:16": "tall portrait 9:16 aspect ratio image",
    "4:3": "landscape 4:3 aspect ratio image",
    "3:4": "portrait 3:4 aspect ratio image"
  };
  var aspectPhrase = aspectMap[aspectStr] || ("aspect ratio " + aspectStr);
  var aspectInjection = "\n\nIMAGE FORMAT REQUIREMENT (STRICT): Render as a "
                       + aspectPhrase + ". Do not change the aspect ratio.";
  var userTextWithAspect = userText + aspectInjection;

  // בנייה דינמית של parts — קודם תמונות הפניה, אחר-כך הטקסט.
  var parts = [];
  if (refImages && refImages.length) {
    for (var ri = 0; ri < refImages.length; ri++) {
      var rim = refImages[ri];
      if (rim && rim.data_base64) {
        parts.push({
          inline_data: {
            mime_type: rim.mime_type || "image/jpeg",
            data: rim.data_base64
          }
        });
      }
    }
  }
  parts.push({ text: userTextWithAspect });
  var out = [];
  var n = Math.max(1, Math.min(count, 4));
  var lastErr = null;
  for (var k = 0; k < n; k++) {
    try {
      var resp = UrlFetchApp.fetch(url, {
        method: "post",
        contentType: "application/json",
        payload: JSON.stringify({
          systemInstruction: { parts: [{ text: systemText }] },
          contents: [{ role: "user", parts: parts }],
          generationConfig: {
            responseModalities: ["IMAGE", "TEXT"],
            imageConfig: { aspectRatio: aspectStr }
          }
        }),
        muteHttpExceptions: true
      });
      if (resp.getResponseCode() !== 200) {
        lastErr = "HTTP " + resp.getResponseCode() + ": " +
                  resp.getContentText().substring(0, 250);
        break;
      }
      var data = JSON.parse(resp.getContentText());
      var cand = (data.candidates && data.candidates[0]) || {};
      var parts = (cand.content && cand.content.parts) || [];
      for (var i = 0; i < parts.length; i++) {
        var inline = parts[i].inline_data || parts[i].inlineData;
        if (inline && inline.data) out.push(inline.data);
      }
    } catch (fetchErr) {
      lastErr = String(fetchErr && fetchErr.message || fetchErr);
      break;
    }
  }
  return { images: out, err: out.length === 0 ? (lastErr || "no images") : null };
}


function _cariGenerateImages(systemText, userText, refImages, aspect, count, apiKey) {
  // מודל יחיד ובלעדי. בלי fallback.
  var r = _cariCallGeminiImage(_CARI_IMAGE_MODEL, systemText, userText, refImages, aspect, count, apiKey);
  if (r.images && r.images.length > 0) return { images: r.images };
  return { images: [], err: _CARI_IMAGE_MODEL + ": " + (r.err || "no images") };
}


// ──────────────────────────────────────────────────────────────────
// Entry point — נקרא מ-Code.gs/doPost כש-prompt_type === 'caricature'.
// ──────────────────────────────────────────────────────────────────
function handleCaricature(body) {
  var startedAt = Date.now();
  // המפתח חייב להגיע מהלקוח. אסור ליפול למפתח השרת — זה היה מאפשר לכל
  // אדם שמכיר את כתובת ה-Apps Script לייצר תמונות על חשבון בעל המוצר.
  var apiKey = body.api_key;
  if (!apiKey || String(apiKey).length < 30) {
    return { error: "no_api_key",
             message: "מפתח Gemini אישי לא סופק. הזן מפתח בהגדרות הבוט." };
  }

  var sceneOriginal = (body.scene_text || "").toString();
  var scene = sceneOriginal;
  var styleKey = body.style_key || "איור מצחיק/הומוריסטי/משעשע";
  var aspect = body.aspect || "1:1";
  var count = parseInt(body.count, 10) || 1;
  var negative = body.negative || "";
  var polish = !!body.polish;
  var requestIp = (body.request_ip || "").toString();
  var userAgent = (body.user_agent || "").toString();

  if (polish && scene.toString().trim()) {
    try {
      var refined = _cariCallGeminiText(
        _cariBuildPolishPrompt(scene, styleKey), apiKey);
      if (refined) scene = refined;
    } catch (polishErr) {
      // ליטוש נכשל — ממשיכים עם הסצנה המקורית
    }
  }

  var req = _cariBuildImageRequest(scene, styleKey, negative);
  // הסצנה המלאה כפי שנשלחת לג'מיני (כולל הפרפיקס הנסתר)
  var sceneWithPrefix = CARI_HIDDEN_PREFIX + scene;
  // תמיד שולחים את הקולאז' המשולב (CARI_REFERENCE_COLLAGE_B64 מ-caricature_reference.gs)
  var refImages = [{
    mime_type: CARI_REFERENCE_COLLAGE_MIME,
    data_base64: CARI_REFERENCE_COLLAGE_B64
  }];
  // אם הלקוח מצרף תמונות הפניה נוספות — להוסיף אחרי הקולאז'
  if (body.reference_images && body.reference_images.length) {
    for (var bi = 0; bi < body.reference_images.length; bi++) {
      refImages.push(body.reference_images[bi]);
    }
  }

  var logEntry = {
    scene_user: sceneOriginal,
    scene_with_prefix: sceneWithPrefix,
    style: styleKey,
    aspect: aspect,
    count_requested: count,
    polish: polish,
    negative: negative,
    request_ip: requestIp,
    user_agent: userAgent,
    // אחרי תיקון האבטחה (גרסה זו ואילך): כל בקשת קריקטורה חייבת
    // להגיע עם מפתח של המשתמש. אם תהיה אי-פעם נפילה למפתח שרת זה
    // יהיה ערך "server_key" ויצביע על באג.
    key_source: "user_key"
  };

  try {
    var result = _cariGenerateImages(req.system, req.user, refImages, aspect, count, apiKey);
    if (!result.images || result.images.length === 0) {
      var msg = "יצירת התמונה לא החזירה תוצאה.";
      if (result.err) {
        if (result.err.indexOf("Service invoked too many times") >= 0 ||
            result.err.indexOf("urlfetch") >= 0 ||
            result.err.indexOf("רוחב") >= 0) {
          msg = "מכסת ה-Apps Script ליום נגמרה. נסה שוב מחר או הפעל בילינג.";
        } else {
          msg += " (" + result.err.substring(0, 200) + ")";
        }
      }
      logEntry.status = "no_images";
      logEntry.images_returned = 0;
      logEntry.error = (result.err || "").toString().substring(0, 500);
      logEntry.duration_ms = Date.now() - startedAt;
      _cariLogRequest(logEntry);
      return { error: "no_images", message: msg };
    }
    logEntry.status = "ok";
    logEntry.images_returned = result.images.length;
    logEntry.error = "";
    logEntry.duration_ms = Date.now() - startedAt;
    _cariLogRequest(logEntry);
    return { images: result.images };
  } catch (err) {
    logEntry.status = "server_error";
    logEntry.images_returned = 0;
    logEntry.error = String(err && err.message || err).substring(0, 500);
    logEntry.duration_ms = Date.now() - startedAt;
    _cariLogRequest(logEntry);
    return { error: "server_error", message: String(err && err.message || err) };
  }
}
