var CARI_HIDDEN_PREFIX = "תאייר לי קריקטורה משעשע והמוריסטי ומצחיק ומוגזם על ";


// ──────────────────────────────────────────────────────────────────
// יומן בקשות הקריקטורה — Google Sheet ייעודי לעיון משה.
// כל בקשה נרשמת ב-SHEET_CARI_LOG (נוצר אוטומטית בריצה ראשונה).
// ──────────────────────────────────────────────────────────────────
var SHEET_CARI_LOG = "יומן_קריקטורות";


function _cariEnsureLogSheet() {
  try {
    var ss = SpreadsheetApp.openById(SPREADSHEET_ID);
    var sheet = ss.getSheetByName(SHEET_CARI_LOG);
    if (!sheet) {
      sheet = ss.insertSheet(SHEET_CARI_LOG);
      sheet.appendRow([
        "timestamp", "scene_user", "scene_with_prefix",
        "style", "aspect", "count_requested", "polish",
        "negative", "status", "images_returned",
        "error", "duration_ms", "request_ip", "user_agent",
        "key_source"
      ]);
      sheet.setFrozenRows(1);
    } else {
      // אם הגיליון כבר קיים בלי key_source — מוסיפים עכשיו עמודה חדשה.
      var headers = sheet.getRange(1, 1, 1, sheet.getLastColumn())
                          .getValues()[0];
      if (headers.indexOf("key_source") < 0) {
        sheet.getRange(1, sheet.getLastColumn() + 1)
             .setValue("key_source");
      }
    }
    return sheet;
  } catch (e) {
    return null;
  }
}


function _cariLogRequest(entry) {
  try {
    var sheet = _cariEnsureLogSheet();
    if (!sheet) return;
    sheet.appendRow([
      new Date(),
      (entry.scene_user || "").toString().substring(0, 5000),
      (entry.scene_with_prefix || "").toString().substring(0, 5000),
      entry.style || "",
      entry.aspect || "",
      entry.count_requested != null ? entry.count_requested : 0,
      !!entry.polish,
      entry.negative || "",
      entry.status || "",
      entry.images_returned != null ? entry.images_returned : 0,
      entry.error || "",
      entry.duration_ms != null ? entry.duration_ms : 0,
      entry.request_ip || "",
      entry.user_agent || "",
      entry.key_source || "user_key"
    ]);
  } catch (e) {
    // לוג נכשל לא יפיל את הבקשה
  }
}


// ──────────────────────────────────────────────────────────────────
// סטטיסטיקת אבטחה — נקרא מ-doGet?action=security_stats כדי לאמוד
// כמה שימוש היה עד כה (לפני שסגרנו את ה-fallback למפתח השרת).
// ──────────────────────────────────────────────────────────────────
function getCaricatureSecurityStats() {
  try {
    var ss = SpreadsheetApp.openById(SPREADSHEET_ID);
    var sheet = ss.getSheetByName(SHEET_CARI_LOG);
    if (!sheet) {
      return { error: "no_log_sheet",
               message: "הגיליון יומן_קריקטורות לא נמצא" };
    }
    var lastRow = sheet.getLastRow();
    var lastCol = sheet.getLastColumn();
    if (lastRow < 2) {
      return { total_requests: 0, total_images_generated: 0,
               by_status: {}, by_month: {},
               first_request: null, last_request: null,
               note: "אין נתונים בגיליון עדיין" };
    }
    var headers = sheet.getRange(1, 1, 1, lastCol).getValues()[0];
    var idxStatus = headers.indexOf("status");
    var idxImages = headers.indexOf("images_returned");
    var idxTs = headers.indexOf("timestamp");
    var idxKey = headers.indexOf("key_source");
    var rows = sheet.getRange(2, 1, lastRow - 1, lastCol).getValues();

    var byStatus = {};
    var byMonth = {};
    var byKey = {};
    var imagesTotal = 0;
    var firstTs = null, lastTs = null;
    for (var i = 0; i < rows.length; i++) {
      var r = rows[i];
      var st = (r[idxStatus] || "").toString();
      byStatus[st] = (byStatus[st] || 0) + 1;
      if (idxImages >= 0) imagesTotal += Number(r[idxImages] || 0);
      var ts = r[idxTs];
      if (ts instanceof Date) {
        if (!firstTs || ts < firstTs) firstTs = ts;
        if (!lastTs || ts > lastTs) lastTs = ts;
        var ym = ts.getFullYear() + "-" +
                 ("0" + (ts.getMonth() + 1)).slice(-2);
        byMonth[ym] = (byMonth[ym] || 0) + 1;
      }
      var ks = (idxKey >= 0 ? (r[idxKey] || "") : "").toString() || "unknown";
      byKey[ks] = (byKey[ks] || 0) + 1;
    }
    return {
      total_requests: rows.length,
      total_images_generated: imagesTotal,
      by_status: byStatus,
      by_month: byMonth,
      by_key_source: byKey,
      first_request: firstTs ? firstTs.toISOString() : null,
      last_request: lastTs ? lastTs.toISOString() : null,
      note: ("עמודת key_source נוספה רק עכשיו — שורות לפני התיקון יסומנו " +
             "ריקות/unknown. כל יצירת תמונה עם status='ok' לפני הגרסה " +
             "הנוכחית הייתה יכולה להיות על מפתח השרת או על מפתח המשתמש.")
    };
  } catch (err) {
    return { error: "exception",
             message: String(err && err.message || err) };
  }
}


