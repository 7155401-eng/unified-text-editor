/**
 * נקודת כניסה: מקבל promptType+body ומחזיר מחרוזת הפרומפט המלאה.
 * נקרא מ-ai_clients.gs/buildPromptByType.
 */
function buildNikudPrompt(promptType, body) {
  var base;
  if (promptType === 'nikud_judge_torah') {
    base = NIKUD_JUDGE_TORAH_PROMPT;
  } else if (promptType === 'nikud_judge_regular') {
    base = NIKUD_JUDGE_REGULAR_PROMPT;
  } else if (promptType === 'nikud_torah') {
    base = NIKUD_TORAH_PROMPT;
  } else {
    base = NIKUD_REGULAR_PROMPT;
  }
  if (body && body.preserve_spelling) {
    base = base + NIKUD_PRESERVE_SPELLING_BLOCK;
  }
  return base;
}
